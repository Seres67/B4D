package fr.B4D.farming;

/**
 * The {@code Profession} enumeration reference all the professions.
 * 
 * @author Lucas
 *
 */
public enum Profession {
	/**
	 * All the professions.
	 */
	Tous,
    
	/**
	 * No profession.
	 */
    Aucun,
    
	/**
	 * Profession of farmer.
	 */
    Paysan,
    
	/**
	 * Profession of miner.
	 */
    Mineur,
    
	/**
	 * Profession of fisher.
	 */
    Pecheur,
    
	/**
	 * Profession of woodcutter.
	 */
    Bucheron,
    
	/**
	 * Profession of alchemist.
	 */
    Alchimiste,
    
	/**
	 * Profession of sculptor.
	 */
    Sculpteur,
    
	/**
	 * Profession of hunter.
	 */
    Chasseur,
    
	/**
	 * Profession of handyman.
	 */
    Bricoleur,
    
	/**
	 * Profession of tailor.
	 */
    Tailleur,
    
	/**
	 * Profession of jeweller.
	 */
    Bijoutier,
    
	/**
	 * Profession of shoemaker.
	 */
    Cordonnier,
    
	/**
	 * Profession of modeler.
	 */
    Faconneur,
    
	/**
	 * Profession of forgemager.
	 */
    Forgemage,
}
